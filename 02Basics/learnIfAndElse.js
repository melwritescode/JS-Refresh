// Display a message based on grade performance
// 6 to 10 - Amazing
// 4 to 5 - Good
// 1 to 3 - Poor
// 0 -  Fail

let grade = 5

if (grade >= 6) {
  console.log('AMAZING')
}
else if (grade >= 4) {
  console.log('Good')
}
else if (grade >= 1) {
  console.log('Poor')
}
else {
  console.log('Fail')
}